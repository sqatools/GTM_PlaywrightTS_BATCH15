//volume of cylinder
var radius = 3
var height = 10
var volume = Math.PI * radius * radius * height
console.log("volume of cylinder: " + volume)    

//surface area of cylinder
var radius = 3
var height = 10
var surfaceArea = 2 * Math.PI * radius * (radius + height)
console.log("surface area of cylinder: " + surfaceArea)

