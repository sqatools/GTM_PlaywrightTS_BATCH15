//data types

//premitive data types  -- immutable in nature
//number
var n1=0;
var n2=8.9;
var n3= 8767;
var n4= 2131312323;
var n5 = -45;

console.log(n1, typeof n1);
console.log(n2, typeof n2);
console.log(n3, typeof n3);
console.log(n4, typeof n4);
console.log(n5, typeof n5);

//string
var s1 ="hi";
