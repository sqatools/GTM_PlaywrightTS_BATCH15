
export function Addition(num1,num2){

    return num1 + num2;
}

export function Multiplication(num1,num2){
    return num1 * num2;
}

export function SqureofNumber(num){
    return num * num;
}

export function Getfactorial(num){

    var fact=1;
    for(var i=num;i>0;i--)
    {
        fact *=i
       
    }
     return fact;
}

